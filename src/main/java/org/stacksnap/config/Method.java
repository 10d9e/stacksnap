package org.stacksnap.config;

public class Method {
	
	private BaseMethod match;
	
	private BaseMethod ignore;

	public BaseMethod getMatch() {
		return match;
	}

	public void setMatch(BaseMethod match) {
		this.match = match;
	}

	public BaseMethod getIgnore() {
		return ignore;
	}

	public void setIgnore(BaseMethod ignore) {
		this.ignore = ignore;
	}
	
	
}
